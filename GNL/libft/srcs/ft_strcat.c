#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int	k;
	int	l;

	k = -1;
	l = (int)ft_strlen(s1);
	while (*(s2 + ++k))
		*(s1 + l++) = *(s2 + k);
	*(s1 + l) = '\0';
	return (s1);
}
